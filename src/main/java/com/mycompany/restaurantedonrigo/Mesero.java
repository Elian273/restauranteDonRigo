    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //declaramos las variables
        String nombreProducto, categoriaProducto;
        double precioProducto;
        
        //obtenemos los datos del formulario
        nombreProducto = txtNombre.getText();
        Object valorPrecio = txtPrecio.getValue();
        precioProducto = (valorPrecio != null) ? ((Number) valorPrecio).doubleValue() : 0.0;
        categoriaProducto = (String) comboCategoria.getSelectedItem();
        
        Producto producto = new Producto(nombreProducto, precioProducto, categoriaProducto);
        
        //agrego el nuevo producto al arrayList
        listaProductos.add(producto);
        
        //mostramos los productos del arrayList
        mostrarProductosTabla();
        
        //limpiar formulario
        txtNombre.setText("");
        txtPrecio.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Fila seleccionada
        int selectedRow = tblMenu.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) tblMenu.getModel();
            model.removeRow(selectedRow);
            listaProductos.remove(selectedRow);
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnOrdenarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarProductoActionPerformed
        //Obtenemos la fila seleccionada
        int fila = tblMenu.getSelectedRow();
        
        if (fila!= -1) 
        {
            //Agregar la informacion al array de ordenes
            Orden ordenActual = new Orden(tblMenu.getValueAt(fila, 0), tblMenu.getValueAt(fila, 1), tblMenu.getValueAt(fila, 2));
            listaOrdenActual.add(ordenActual);
        }

        //actualizo el valor total de la orde
        double acumPrecioTotal = 0;
        //recorro el arrar listaOrdenActual y sumo los precios
        for(Orden orden : listaOrdenActual)
        {
            acumPrecioTotal = acumPrecioTotal + orden.getPrecio();
        }
        
        //mostrar el acumulado en el lbl
        lblPrecioTotal.setText(String.valueOf(acumPrecioTotal));
        
        //Mostramos la informacion del array en la tabla
        mostrarOrdenesTabla();
    }//GEN-LAST:event_btnOrdenarProductoActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        //Eliminar la informacion del arrayList listaOrdenActual
        listaOrdenActual.clear();
        
        //limpiar la tabla
        ordenesModel.setRowCount(0);
        tblOrdenes.setModel(ordenesModel);
        
        //limpiar el lbl
        lblPrecioTotal.setText("");
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        //instancia de cocina y le paso la informacion al metodo recibirArrayOrdenes
        String mesa;
        mesa = (String) comboMesa.getSelectedItem();
        
        Cocina cocina = new Cocina();
        
        cocina.recibirArrayOrdenes(listaOrdenActual, mesa);
                
        cocina.setVisible(true);
    }//GEN-LAST:event_btnOrdenarActionPerformed


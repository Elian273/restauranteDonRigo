    //Recibimos la informacion desde Agregar Menu
    public void mostrarProductosTabla() 
    {
        //limpiamos la tabla
        menuModel.setRowCount(0);
        
        //Agregar los productos al modelo
        for (Producto producto : listaProductos) {
            Object[] fila = new Object[3];
            fila[0] = producto.getNombre();
            fila[1] = producto.getPrecio();
            fila[2] = producto.getCategoria();
            menuModel.addRow(fila);
        }
        
        //Establecer el modelo en la tabla
        tblMenu.setModel(menuModel);
    }
    
    //Recibimos la informacion desde Agregar Menu
    public void mostrarOrdenesTabla() 
    {
        //limpiamos la tabla
        ordenesModel.setRowCount(0);
        
        //Agregar los productos al modelo
        for (Orden orden : listaOrdenActual) {
            Object[] fila = new Object[3];
            fila[0] = orden.getNombre();
            fila[1] = orden.getPrecio();
            fila[2] = orden.getCategoria();
            ordenesModel.addRow(fila);
        }
        
        //Establecer el modelo en la tabla
        tblOrdenes.setModel(ordenesModel);
    }

